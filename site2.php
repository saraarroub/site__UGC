            document.addEventListener('DOMContentLoaded', function() {
                const section = document.querySelector('#partie4');
                const buttons = section.querySelectorAll('.button');
                const videos = section.querySelectorAll('video');
                const slideDivs = section.querySelectorAll('.slide_div');
                const pauseButtons = section.querySelectorAll('.pause-btn');

                // Function to pause all videos and reset currentTime
                function pauseAllVideos() {
                    videos.forEach(video => {
                        video.pause();
                        video.currentTime = 0; 
                    });
                }

                // Add event listeners to standard pause buttons
                pauseButtons.forEach((pauseButton, index) => {
                    pauseButton.addEventListener('click', (event) => {
                        event.preventDefault(); // Prevent default behavior
                        const targetVideo = videos[index];

                        if (targetVideo.paused) {
                            targetVideo.play();
                            pauseButton.textContent = 'Pause';
                        } else {
                            targetVideo.pause();
                            pauseButton.textContent = 'Play';
                        }
                    });
                });

                // Add event listeners to enlarge corresponding slide_divs on video click
                videos.forEach((video, index) => {
                    video.addEventListener('click', () => {
                        pauseAllVideos();
                        video.play();

                        slideDivs.forEach(slideDiv => {
                            slideDiv.classList.remove('larger');
                        });

                        slideDivs[index].classList.add('larger');
                    });
                });

                // Ensure videos are muted by default
                videos.forEach(video => {
                    video.muted = true;
                });
            });

        </script>
